#include <iostream>
using namespace std;


int main() {
    int a, b;
    cin >> a >> b;
    
    if (a < 0 || b < 0  || a > b) {
        cout << "Input Salah";
    }
    
    else {
        for (int i = a; i <= b; i++) {
            if (i == 0) {
                cout << "0 ";
            }
            else if (i % 18 == 0 && i % 15 == 0) {
                cout << "Lima Belas ";
            }
            else if (i % 18 == 0) {
                cout << "Delapan Belas ";
            }
            
            else if (i % 15 == 0) {
                cout << "Lima Belas ";
            }
            
            else cout << i << " ";
        }
    } 
    return 0;
}
